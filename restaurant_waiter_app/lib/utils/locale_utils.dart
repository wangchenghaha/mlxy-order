  static Future<void> changeLanguage(String lang) async {
    if (HiveUtils.lang == lang) return;
    HiveUtils.lang = lang;
    await load(notifyBeforeFetch: true);
  }

  static Future<void> load({bool notifyBeforeFetch = false}) async {
    final requestLang = HiveUtils.lang;
    _messages = HiveUtils.i18n;
    if (notifyBeforeFetch) {
      _notifyRebuild();
    }
    try {
      final nextMessages = await I18nApi.list(requestLang);
      await HiveUtils.saveI18n(requestLang, nextMessages);
      if (HiveUtils.lang != requestLang) return;
      _messages = nextMessages;
    } catch (_) {
      if (HiveUtils.lang != requestLang) return;
      _messages = HiveUtils.i18n;
    }
    _notifyRebuild();
  }

  static void _notifyRebuild() {
    rebuildNotifier.value++;
  }

  static String get(String key) =>
      _messages[key] ??
      _fallbackMessages[HiveUtils.lang]?[key] ??
      _fallbackMessages['en_us']?[key] ??
      key;
}
